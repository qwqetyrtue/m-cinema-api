    /**
     * 加锁
     *
     * @param key       键
     * @param timeStamp 当前时间 + 超时时间
     * @param threadId        该线程的唯一标志
     * 此id可以用这个方法获取 String threadId = BaseUtils.uuid();
     * @return 是否拿到锁
     */
    public boolean lock(String key, String timeStamp, String threadId) {
        String value = timeStamp + "_" + threadId;
        if (redisTemplate.opsForValue().setIfAbsent(key, value)) {
            // TODO: 2021/6/25 初始化的时候, 竞争锁
            return true;
        }
        // todo : 判断锁超时 - 防止原来的操作异常，没有运行解锁操作  防止死锁
        String redisValue = redisTemplate.opsForValue().get(key) + "";
        // todo : 如果锁过期
        if (StringUtils.isNotEmpty(redisValue) && StringUtils.contains(redisValue, "_")) {
            String[] redisValues = redisValue.split("_");
            long redisTimeStamp = Long.parseLong(redisValues[0]);
            // todo : 锁过期
            if (redisTimeStamp < System.currentTimeMillis()) {
                String oldValue = redisTemplate.opsForValue().getAndSet(key, value) + "";
                // todo : 是否已被别人抢占
                boolean r = StringUtils.isNotEmpty(oldValue) && oldValue.equals(redisValue);
                if (r) {
                    // TODO: 2021/6/25 最多锁住 3 秒钟, 自动释放, 把成功拿到锁的线程信息, 写进入
                    redisTemplate.opsForValue().set(key, value, 3, TimeUnit.SECONDS);
                }
                return r;
            }
        }
        return false;
    }

    /**
     * 解锁
     *
     * @param key       键
     * @param timeStamp 当前时间 + 超时时间
     * @param id        该线程的唯一标志
     */
    public void unlock(String key, String timeStamp, String id) {
        try {
            String redisValue = redisTemplate.opsForValue().get(key) + "";
            if (StringUtils.isNotEmpty(redisValue) && redisValue.equals(timeStamp + "_" + id)) {
                redisTemplate.opsForValue().getOperations().delete(key);
            }
        } catch (Exception e) {
            logger.error("redis unlock error,unlock 解锁的Key="+key);
        }
    }
